    private void JlogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JlogarActionPerformed

        if( cBlog.getSelectedItem().equals("ADIMINISTRADOR")){
            String login= jLogin.getText();
            String senha= jSenha.getText();
            AcessoDAO sql = new AcessoDAO();
            if("".equals(login) && "".equals(senha)){
                JOptionPane.showMessageDialog(this, "Preencha os campos corretamente");
            }else{
                if(sql.LogarAdm(login, senha) == true){
                    new Thread(){
                        public void run(){
                            for (int i=0; i<101; i++){
                                jProgressBar1.setValue(i);
                                try{
                                    Thread.sleep(35);
                                } catch (Exception ex) {
                                    ex.getMessage();
                                }

                            }
                            new AreaAdm().setVisible(true);
                            dispose();
                        }
                    }.start();
                } else{
                    JOptionPane.showMessageDialog(this, "UsuÃ¡rio inexistente");
                }
            }
        }else if( cBlog.getSelectedItem().equals("CLIENTE")){
            String login= jLogin.getText();
            String senha= jSenha.getText();
            AcessoDAO sql = new AcessoDAO();
            if("".equals(login) && "".equals(senha)){
                JOptionPane.showMessageDialog(this, "Preencha os campos");
            }else{
                if(sql.LogarClie(login, senha) == true){
                    new Thread(){
                        public void run(){
                            for (int i=0; i<101; i++){
                                jProgressBar1.setValue(i);
                                try{
                                    Thread.sleep(35);
                                } catch (Exception ex) {
                                    ex.getMessage();
                                }

                            }
                            new AreaClie().setVisible(true);
                            dispose();
                        }
                    }.start();
                } else{
                    JOptionPane.showMessageDialog(this, "Usuario inesistente");
                }
            }
    }//GEN-LAST:event_JlogarActionPerformed
    }
